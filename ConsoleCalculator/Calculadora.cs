
class Calculadora
{
    public decimal Somar(decimal Input_1, decimal Input_2)
    {
        return Input_1 + Input_2;
    }
    public decimal Subtracao(decimal Input_1, decimal Input_2)
    {
        return Input_1 - Input_2;
    }
    public decimal Multiplicacao(decimal Input_1, decimal Input_2)
    {
        return Input_1 * Input_2;
    }
    public decimal Divisao(decimal Input_1, decimal Input_2)
    {
        return Input_1 / Input_2;
    }

}